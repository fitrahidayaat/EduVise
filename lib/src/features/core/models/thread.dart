class Thread {
  final String name;
  final String description;
  final String image;
  final int depth;
  final int index;
  final int likes;
  final int dislikes;

  Thread({
    required this.name,
    required this.description,
    required this.image,
    required this.depth,
    required this.index,
    required this.likes,
    required this.dislikes,
  });
}
